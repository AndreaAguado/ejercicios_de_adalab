'use strict';

const todayDate = 'Sábado';

switch (todayDate) {
  case 'Lunes':
    console.log('Llorar mucho y muy fuertemente por la mañana');
    break;
  case 'Martes':
    console.log('Hoy toca comer pasta');
    break;
  case 'Miércoles':
    console.log('Estamos lejos del viernes pero más cerca que ayer');
    break;
  case 'Jueves':
    console.log('Ya sólo queda un madrugón');
    break;
  case 'Viernes':
    console.log('TGIF!!!!!!!!!!');
    break;
  case 'Sábado':
    console.log('Descansar por la tarde');
    break;
  default:
    console.log('Dormir hasta las 12');
}